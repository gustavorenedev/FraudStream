using FraudStream.Api.Middlewares;
using FraudStream.Application;
using FraudStream.Infrastructure;
using FraudStream.Infrastructure.Persistence;
using Microsoft.EntityFrameworkCore;
using Serilog;

var builder = WebApplication.CreateBuilder(args);

// ?? Camadas ????????????????????????????????????????????????????????????????????
builder.Services
    .AddApplication()
    .AddInfrastructure(builder.Configuration);

// ?? API ????????????????????????????????????????????????????????????????????????
builder.Services.AddControllers();
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen(options =>
{
    options.SwaggerDoc("v1", new()
    {
        Title = "FraudStream API",
        Version = "v1",
        Description = "Pipeline de detecção de fraude em tempo real — MVP"
    });
});

// ?? Logging estruturado (Serilog via appsettings) ?????????????????????????????
builder.Host.UseSerilog((ctx, cfg) =>
    cfg.ReadFrom.Configuration(ctx.Configuration));

var app = builder.Build();

// ?? Migrations automáticas na inicialização ???????????????????????????????????
// Garante que o banco esteja atualizado sem rodar dotnet ef manualmente
using (var scope = app.Services.CreateScope())
{
    var db = scope.ServiceProvider.GetRequiredService<FraudStreamDbContext>();
    await db.Database.MigrateAsync();
}

// ?? Pipeline HTTP ??????????????????????????????????????????????????????????????
app.UseMiddleware<ExceptionHandlingMiddleware>();

if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI(options =>
    {
        options.SwaggerEndpoint("/swagger/v1/swagger.json", "FraudStream API v1");
        options.RoutePrefix = "swagger";
    });
}

app.UseHttpsRedirection();
app.MapControllers();

await app.RunAsync();